CREATE TABLE  benefitpoint.audit_custom_section (
	 customization_area 					TEXT 		NOT NULL 
    ,"label"						    	TEXT 		NOT NULL 
	,old_sort_order 						int 		NULL 
    ,new_sort_order 						int 		NULL 
	,old_active 							bool 		NULL 
    ,new_active 							bool 		NULL 
    ,old_account_customization_categories 	TEXT 		NULL 
    ,new_account_customization_categories 	TEXT 		NULL 
	,audit_action                           char(1) 	NOT NULL 
	,audit_user 							TEXT 		NOT NULL 	DEFAULT current_user
